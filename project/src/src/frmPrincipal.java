    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed

        int counterAlfabet = 0;
        int counterQf = 0;
        int counterQ = 0;
        int alfabetLength = 0;
        int finalStateLength = 0;
        int stateLength = 0;

        String filePath = "src\\files\\tabla.txt";

        File file = new File(filePath);

        try {
            BufferedReader br = new BufferedReader(new FileReader(file));
            String firstLine = br.readLine().trim();
            String[] columns = firstLine.split(";");
            DefaultTableModel model = (DefaultTableModel) tblTransicion.getModel();
            model.setColumnIdentifiers(columns);

            //Storing alfabet's length
            for (int i = 2; i < columns.length; i++) {
                alfabetLength++;
            }

            //Assigning length of alfabet
            array_alf = new String[alfabetLength];

            //Storing the alfabet
            for (int i = 2; i < columns.length; i++) {
                array_alf[counterAlfabet] = columns[i].toString();
                counterAlfabet++;
            }

            Object[] rows = br.lines().toArray();

            for (int i = 0; i < rows.length; i++) {
                String line = rows[i].toString().trim();
                String[] dataRow = line.split(";", -1);
                stateLength++; //Setting length of states array
                if (dataRow[1].equals("Si") || dataRow[1].equals("SI") || dataRow[1].equals("sI") || dataRow[1].equals("si")) {
                    finalStateLength++; //Setting length of final states array
                }
                for (int j = 2; j < columns.length; j++) {

                    //System.out.println(dataRow[j]);
                    if (dataRow[j].equals("-") || dataRow[j].equals("")) {
                        dataRow[j] = "";
                    }
                }
                model.addRow(dataRow);
            }

            //Assigning length of states array
            array_q = new String[stateLength];

            //Assigning length of final states array
            array_qf = new String[finalStateLength];

            //Storing final states
            for (int i = 0; i < rows.length; i++) {
                String line = rows[i].toString().trim();
                String[] dataRow = line.split(";");
                //Getting first state
                if (i == 0) {
                    q0 = dataRow[0];
                }
                //Getting states
                array_q[counterQ] = dataRow[0].trim();
                counterQ++;
                if (dataRow[1].equals("Si") || dataRow[1].equals("SI") || dataRow[1].equals("sI") || dataRow[1].equals("si")) {
                    //Getting final states
                    array_qf[counterQf] = dataRow[0].trim();
                    counterQf++;
                }
            }
        } catch (FileNotFoundException ex) {
            java.util.logging.Logger.getLogger(frmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            java.util.logging.Logger.getLogger(frmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnGenerarActionPerformed

    private void btnEvaluarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEvaluarActionPerformed

        String cadena = txtCadena.getText();

        if (cadena.equals(null) || "".equals(cadena)) {
            JOptionPane.showMessageDialog(null, "Inserte una cadena para evaluar", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
        if (tblTransicion.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Genere la tabla antes de evaluar una cadena", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
        lista_alf = Lists.newArrayList(array_alf);
        lista_q = Lists.newArrayList(array_q);
        lista_qf = Lists.newArrayList(array_qf);

        trans = new transicion[tblTransicion.getRowCount()][tblTransicion.getColumnCount() - 2];
        //System.out.println("rowCount ->" + tblTransicion.getRowCount() + "\nColumnCount ->" + tblTransicion.getColumnCount());
        for (int i = 0; i < trans.length; i++) {
            for (int j = 0; j < trans[i].length; j++) {
                if (tblTransicion.getValueAt(i, j + 2).toString().equals("null") || tblTransicion.getValueAt(i, j + 2).toString().equals("-") || tblTransicion.getValueAt(i, j + 2).toString().equals(" ")) {
                    trans[i][j] = new transicion(lista_q.get(i), lista_alf.get(j), null);
                } else {
                    trans[i][j] = new transicion(lista_q.get(i), lista_alf.get(j), tblTransicion.getValueAt(i, j + 2).toString());
                    //System.out.println("value at (" + i + "," + (j + 1) + ") -> " + tblTransicion.getValueAt(i, j + 2).toString());
                }
                if (lista_q.get(i).equals(q0)) {
                    fila_q0 = i;
                }
            }
        }

        String cad = txtCadena.getText(); //Stores value of the string to be tested
        char[] cad_rev = cad.toCharArray();
        List<String> lista_cad = new ArrayList<String>();
        for (char obj : cad_rev) {
            lista_cad.add(Character.toString(obj));
        }

        n = cad.length();
        aux_ns = 0;
        String next_state = null;
        boolean aceptado = true;
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < lista_alf.size(); j++) {
                if (trans[fila_q0][j].getLetra().equals(String.valueOf(cad.charAt(0)))) {
                    next_state = trans[fila_q0][j].getEstado_obj();
                    if (next_state == null) {
                        JOptionPane.showMessageDialog(null, "Imposible continuar, transicion inexistente");
                        aceptado = false;
                    } else {
                        for (int k = 0; k < lista_q.size(); k++) {
                            if (lista_q.get(k).equals(next_state)) {
                                fila_q0 = k;
                            }
                        }
                    }
                }
            }
            cad = cad.substring(1, cad.length());
            if (aceptado == false) {
                next_state = "error";
                break;
            }
        }
        for (int i = 0; i < lista_qf.size(); i++) {
            if (next_state.equals(lista_qf.get(i))) {
                Font f = txtResult.getFont();
                txtResult.setForeground(Color.green);
                txtResult.setFont(f.deriveFont(f.getStyle() | Font.BOLD));
                txtResult.setText("Aceptado"); //color verde
                txtFinalState.setText(next_state);
                System.out.println("All good üòÉ");
                break;
            } else {
                Font f = txtResult.getFont();
                txtResult.setForeground(Color.red);
                txtResult.setFont(f.deriveFont(f.getStyle() | Font.BOLD));
                txtResult.setText("Rechazado"); //color rojo
                txtFinalState.setText(next_state);
                System.out.println("Check the transitions üëÄ");
            }
        }

    }//GEN-LAST:event_btnEvaluarActionPerformed

